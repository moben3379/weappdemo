package cn.edu.guet.weappdemo.bean;



public class FoodMenu {
    private Integer Menu_Id;
    private String Menu_Name;
    private double Menu_Price;
    private String Menu_Form;
    private Integer Menu_Count;
    private String Menu_Icon;
    private String Menu_detail;

    public int getMenu_Id() {
        return Menu_Id;
    }

    public void setMenu_Id(int menu_Id) {
        Menu_Id = menu_Id;
    }

    public String getMenu_Name() {
        return Menu_Name;
    }

    public void setMenu_Name(String menu_Name) {
        Menu_Name = menu_Name;
    }

    public double getMenu_Price() {
        return Menu_Price;
    }

    public void setMenu_Price(double menu_Price) {
        Menu_Price = menu_Price;
    }

    public String getMenu_Form() {
        return Menu_Form;
    }

    public void setMenu_Form(String menu_Form) {
        Menu_Form = menu_Form;
    }

    public int getMenu_Count() {
        return Menu_Count;
    }

    public void setMenu_Count(int menu_Count) {
        Menu_Count = menu_Count;
    }

    public String getMenu_Icon() {
        return Menu_Icon;
    }

    public void setMenu_Icon(String menu_Icon) {
        Menu_Icon = menu_Icon;
    }

    public String getMenu_detail() {
        return Menu_detail;
    }

    public void setMenu_detail(String menu_detail) {
        Menu_detail = menu_detail;
    }

}
